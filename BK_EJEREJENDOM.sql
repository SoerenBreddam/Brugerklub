				QL65000V_T.TEKST AS Administratorstatus
		FROM
				QL61500V_T LEFT OUTER JOIN
				QL63400V_T LEFT OUTER JOIN
				QL63700V_T RIGHT OUTER JOIN
				JY67500V_T INNER JOIN
				JY64900V_T INNER JOIN
				CO11800T ON JY64900V_T.EEJDNR = CO11800T.EJD_NR ON JY67500V_T.EEJDNR = JY64900V_T.EEJDNR ON QL63700V_T.KODE = JY64900V_T.COVMD ON QL63400V_T.KODE = JY64900V_T.CPENS LEFT OUTER JOIN
				QL63200V_T ON JY64900V_T.CMSTAT = QL63200V_T.KODE LEFT OUTER JOIN
				QL63500V_T ON JY64900V_T.CLEJE = QL63500V_T.KODE LEFT OUTER JOIN
				QL50000V_T ON JY64900V_T.CEJFORH = QL50000V_T.KODE LEFT OUTER JOIN
				QL63600V_T ON JY64900V_T.CBSKADR = QL63600V_T.KODE RIGHT OUTER JOIN
				QL65000V_T ON JY67500V_T.CADM = QL65000V_T.KODE RIGHT OUTER JOIN
				QL61000V_T ON JY67500V_T.CARTK = QL61000V_T.KODE ON QL61500V_T.KODE = JY67500V_T.CBENYT) AS Ejertabel
	RIGHT OUTER JOIN MAT_Aktiv_SamletFastEjendom ON Ejertabel.BFE_NUMMER = MAT_Aktiv_SamletFastEjendom.BFEnr