				JY67500V_T.FEJER AS Antal_ejere,
				JY67500V_T.FLEJL AS Antal_lejligheder,
				JY67500V_T.EMODER AS Moderejendomsnummer,
				JY67500V_T.COFVEJ AS Adgang_til_offentlig_vej,
